#include "libft.h"

void	ft_putnbr(int nb)
{
	long	nbl;

	nbl = nb;
	if (nbl < 0)
	{
		ft_putchar('-');
		nbl = nbl * -1;
	}
	if (nbl / 10 > 0)
	{
		ft_putnbr(nbl / 10);
	}
	ft_putchar('0' + nbl % 10);
}
